import { browserHistory } from 'react-router';

export default function handleApiError(error) {
  if (error.street_address) {
    window.alert('There was a problem with that street address. Please ensure it is not in an active campaign.');
    browserHistory.push('/');
  } else {
    window.alert('There was an issue with your request. Please try again later.');
    browserHistory.push('/');
  }
}
